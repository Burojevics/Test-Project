package class3;

public class RelationalOperators_6 {
    public static void main(String[] args) {
        /* Arithmetic operator
        int + int= int
        int/int= int
        int-int= int
         */

        /* Relational operator always responds with TRUE or FALSE
         */
        int number=10;
        int number2=10;
        System.out.println(number+number2);

        boolean result=20>10; //is 20 greater than 10
        /*
        == equal to
        != not equal to
        > greater than
        < less than
        >= greater or equal
        <= less or equal
         */
        System.out.println(result);
        System.out.println(5!=10); //is number 5 not equal to 10
        System.out.println(5==10);// is number 5 equal to 10
        System.out.println(10>=20); // is greater or equal to 20







    }
}
